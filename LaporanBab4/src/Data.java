public class Data {
    private String nama;
    private String alamat;
    
    public void setNama (String name){
        this.nama = name;
    }
    public String getNama (){
        return nama;
    }
    public void setAlamat (String address){
        this.alamat = address;
    }
    public String getAlamat (){
        return alamat;
    }
            
}
